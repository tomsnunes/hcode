<?php

$a = 55.0;
$b = 55;

//Operadores de Comparação
//Maior que
var_dump($a > $b);
echo "<br>";

//Menor que
var_dump($a < $b);
echo "<br>";

//Igual
var_dump($a == $b);
echo "<br>";

//Idêntico
var_dump($a === $b);
echo "<br>";

//Não Igual
var_dump($a != $b);
echo "<br>";

//Não Idêntico
var_dump($a !== $b);
echo "<br>";
?>